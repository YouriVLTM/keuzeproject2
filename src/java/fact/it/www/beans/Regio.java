package fact.it.www.beans;

/**
 *
 * @author u0068237
 */
public class Regio {
    private int id;
    private String naam;

    public Regio() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    @Override
    public String toString() {
        return "Regio{" + "id=" + id + ", naam=" + naam + '}';
    }
    

}